function showVideo(id){
    var video = document.getElementById(id);
    console.log("***********************"+video.style.display);
    video.style.display= 'block' 
    video.play();
}
function removeVideo(id){
    var video = document.getElementById(id);
    console.log("***********************"+video.style.display);
    video.style.display= 'none'
    video.onpause();
}

